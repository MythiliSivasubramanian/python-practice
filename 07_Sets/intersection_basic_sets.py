# Basic Set Intersection


# Create 2 sets a and b with 2 common elements
set_a = {1, 2, 3, 4, 5}
set_b = {4, 5, 6, 7}

# & gives the common elements from both sets
common = set_a & set_b

# Printing the sets a and b
print("\nSet A:", set_a)
print("\nSet B:", set_b)
print("\nCommon Elements set_a & set_b : ", common,"\n")