# Creating a dictionary, adding a new key value pair  and updating an exsisting value using [] 

# Creating a dictionary with 2 Key Value pairs
student = {
    "name": "Rahul",
    "age": 21
}
print("\nActual Dictionary is : ",student)

# Adding a new key-value pair using []
student["course"] = "Python"

# Updating an existing value using []
student["age"] = 22

# Priting finial dictionary
print("\nUpdated Dictionary is : ",student)
